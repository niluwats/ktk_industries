    private void btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddActionPerformed
      String seq= null;   
         String seq1= null;   
         String seq2= null;  
        try {
            ResultSet rs = DB.search("select * from user where username='"+username+"'");
         
            if (rs.next()) {
              seq=rs.getString("Seq1_ans");
              seq1=rs.getString("Seq2_ans");
              seq2=rs.getString("Seq3_ans");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (!seq.equals(seq1ans.getText())) {
           JOptionPane.showMessageDialog(this, "Sequrity Question 1 Answer Doesn't match", " Please enter a correct answer", JOptionPane.INFORMATION_MESSAGE);
        }else if(!seq1.equals(seq2ans.getText())){
            JOptionPane.showMessageDialog(this, "Sequrity Question 2 Answer Doesn't match", " Please enter a correct answer", JOptionPane.INFORMATION_MESSAGE);
        }else if(!seq2.equals(seq3ans.getText())){
           JOptionPane.showMessageDialog(this, "Sequrity Question 3 Answer Doesn't match", " Please enter a correct answer", JOptionPane.INFORMATION_MESSAGE);
        }else{
               Password_change pc = new Password_change();
                pc.setVisible(true);
                this.dispose();
                
        }
        
    }//GEN-LAST:event_btn_AddActionPerformed


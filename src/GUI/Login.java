    private void load_email() {
        Properties p = new Properties();
        FileReader fr = null;
        try {
            fr = new FileReader("sys_mail.config");
        } catch (FileNotFoundException ex) {
            java.util.logging.Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            p.load(fr);
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

        String email = p.getProperty("Sys_Email");
        String password = p.getProperty("Password");

        sys_email = (email);
        sys_pass = (password);
    }
    public void trylog(){
    Model.Log4jTest.intiLogger();
        org.apache.log4j.Logger logger = org.apache.log4j.Logger.getLogger("loggerdata");
        if (userName.getText().isEmpty() | password.getText().toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all the fields", "Feilds not filled", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                ResultSet rs = DB.search("select * from user where username='nima'");
               
                if (rs.next()) {
                    if(rs.getInt("state")==1){
                    logger.info("'" + userName.getText() + "' logged to system");
                    Main_menu m = new Main_menu(userName.getText());
                    m.setVisible(true);
                    dispose();
                    }else{
                     JOptionPane.showMessageDialog(this, "User Deactivated", "User Deactive", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Username or Password invalid", "Invalid username or Password"
                            , JOptionPane.INFORMATION_MESSAGE);
                    logger.warn("user try to logging to system with username='" + userName.getText() + "' and password='" 
                            + password.getText().toString() + "'");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }

    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        Vector v = new Vector();
        v.add(aod_no.getSelectedItem());
        v.add(customer.getText());
        v.add(value.getText());

        double d1 = Double.parseDouble(value.getText());
        if (!tot_value.getText().equals("")) {
            double tot = Double.parseDouble(tot_value.getText()) + Double.parseDouble(value.getText());
            tot_value.setText("" + tot);
        } else {
            tot_value.setText(value.getText());
        }

        aod_no.setSelectedItem(null);
        customer.setText(null);
        value.setText(null);

        dtm.addRow(v);
    }//GEN-LAST:event_btn_SaveActionPerformed


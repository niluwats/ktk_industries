    private void searchBoxKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBoxKeyReleased
        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        try {
            ResultSet rs = DB.search("SELECT product.id_product,product_brand.brand,product_category.category,product.productname,product.unit,FORMAT(product.retail_price,2) AS price "
                    + "FROM product INNER JOIN product_brand ON product.brand=product_brand.idbrand INNER JOIN product_category ON product.category=product_category.idcategory "
                    + "WHERE product.productname like '" + searchBox.getText() + "%' OR product.id_product like '" + searchBox.getText() + "%' OR product_brand.brand like '" + searchBox.getText() + "%' OR product_category.category like '" + searchBox.getText() + "%' AND product.state='1'");
            int rowNo = 1;
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rowNo++);
                v.add(" " + rs.getString("id_product"));
                v.add(" " + rs.getString("brand"));
                v.add(" " + rs.getString("category"));
                v.add(" " + rs.getString("productname"));
                v.add(rs.getString("unit"));
                v.add(rs.getString("price"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchBoxKeyReleased


    private void qtyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_qtyActionPerformed

        boolean b = true;
        for (int i = 0; i < table.getRowCount(); i++) {
            if (table.getValueAt(i, 0).equals(productCode.getText())) {
                b = false;
                double addqty = Double.parseDouble(qty.getText());
                double getqty = Double.parseDouble(table.getValueAt(i, 3).toString());
                table.setValueAt(addqty + getqty, i, 3);
            }
        }
        if (b) {
            if (!(productCode.getText().isEmpty() | productName.getText().isEmpty() | qty.getText().isEmpty() | unit.getText().isEmpty())) {
                try {
                    DefaultTableModel dtm = (DefaultTableModel) table.getModel();
                    Vector v = new Vector();
                    v.add(productCode.getText());
                    v.add(productName.getText());
                    v.add(unit.getText());
                    v.add(qty.getText());
                    dtm.addRow(v);

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
        productCode.setText(null);
        productName.setText(null);
        unit.setText(null);
        qty.setText(null);
        productCode.grabFocus();
    }//GEN-LAST:event_qtyActionPerformed


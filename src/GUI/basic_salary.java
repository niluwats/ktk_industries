    private void tableSettings() {
        JTableHeader tableHeader = jTable1.getTableHeader();
        tableHeader.setFont(new Font("Calibri", Font.BOLD, 14));
        ((DefaultTableCellRenderer) tableHeader.getDefaultRenderer()).setHorizontalAlignment(JLabel.CENTER);

        DefaultTableCellRenderer center = new DefaultTableCellRenderer();
        center.setHorizontalAlignment(JLabel.CENTER);

        DefaultTableCellRenderer right = new DefaultTableCellRenderer();
        right.setHorizontalAlignment(JLabel.RIGHT);

        jTable1.getColumnModel().getColumn(2).setCellRenderer(center);
        jTable1.getColumnModel().getColumn(3).setCellRenderer(right);
    }

    private void loadTable() {
        emp_id.setText(null);
        emp_name.setText(null);
        basic_salary.setText(null);
        salary_period.setText(null);
        try {
            ResultSet rs = DB.search("SELECT employee.id_employee,fname,mname,basicSalary,salary_period FROM employee LEFT JOIN basicsalary ON employee.id_employee=basicsalary.id_employee WHERE  department='" + department_name.getSelectedItem().toString() + "' AND status='1'");
            //select *  from basicsalary where id_employee='EMP0005' and saved_date=(select max(saved_date) from basicsalary)
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("employee.id_employee"));
                v.add(rs.getString("fname"));
                v.add(rs.getString("mname"));
                v.add(rs.getString("basicSalary"));
                v.add(rs.getString("salary_period"));
                dtm.addRow(v);
            }
            tableSettings();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

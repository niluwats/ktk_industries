    private void tableSettings() {
        JTableHeader tableHeader = table.getTableHeader();
        tableHeader.setFont(new Font("Calibri", Font.BOLD, 14));
        ((DefaultTableCellRenderer) tableHeader.getDefaultRenderer()).setHorizontalAlignment(JLabel.CENTER);

        DefaultTableCellRenderer center = new DefaultTableCellRenderer();
        center.setHorizontalAlignment(JLabel.CENTER);

        table.getColumnModel().getColumn(0).setCellRenderer(center);
        table.getColumnModel().getColumn(1).setCellRenderer(center);
        table.getColumnModel().getColumn(3).setCellRenderer(center);
        table.getColumnModel().getColumn(4).setCellRenderer(center);
        table.getColumnModel().getColumn(5).setCellRenderer(center);

    }

    private void loadData() {

        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        try {
            ResultSet rs = DB.search("SELECT job_id,req_department,start_date,finish_date,approved,state FROM bill_of_matrial");
            int rowNo = 1;
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rowNo++);
                v.add(rs.getString("job_id"));
                v.add(rs.getString("req_department"));
                v.add(rs.getString("start_date"));
                v.add(rs.getString("finish_date"));
                if (rs.getInt("approved") == 0) {
                    v.add("Not Approved");

                } else if (rs.getInt("approved") == 1) {
                    v.add("Approved");
                }
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


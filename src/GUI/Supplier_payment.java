    private void supplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supplierActionPerformed
        try {
            // double total = 0;
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            ResultSet rs = DB.search("select * from supplier where company='" + supplier.getSelectedItem() + "'");
            if (rs.next()) {
                supid.setText(rs.getString("id_supplier"));
                amount.setText(null);
                ResultSet r = DB.search("select * from grn_raw where id_supplier = '" + supid.getText() + "' and state = '" + 0 + "' ");
                while (r.next()) {
                    Vector v = new Vector();
                    v.add(r.getRow());
                    v.add(r.getString("grn_no"));
                    v.add(r.getString("date"));
                    v.add(r.getString("value"));
                    df.addRow(v);
                int total = 0;
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                       int amount = Integer.parseInt((String) jTable1.getValueAt(i, 3));
                       total += amount;
                    }
                amount.setText("" + total);
                }}

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_supplierActionPerformed


    private void genarateid() {
        try {
            ResultSet rs = DB.search("SELECT COUNT(payment_voucherNo) as id FROM supplier_payment");
            if (rs.next()) {
                int count = rs.getInt("id");
                count++;
                voucher_no.setText("SPV" + String.format("%0" + 6 + "d", count));
            }
            supplier.grabFocus();
        } catch (Exception e) {

        }
    }

    private void onload() {
        try {
            ResultSet rs1 = DB.search("select * from supplier ");
            while (rs1.next()) {
                supplier.addItem(rs1.getString("company"));
            }
            SimpleDateFormat sd = new SimpleDateFormat("yyyy-MM-dd");
            Date d = new Date();
            Sys_date.setText(sd.format(d));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clear() {
      DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
df.setRowCount(0);
supplier.setSelectedItem(null);
supid.setText(null);
amount.setText(null);
genarateid();
    }

    private void ReportGen() {
        try {
            String reportSourse = "iReport\\supplier_payment.jrxml";
            Map<String, Object> params = new HashMap<String, Object>();
            params.put("voucher_no", voucher_no.getText());
            JasperReport jaspareport = JasperCompileManager.compileReport(reportSourse);
            Connection connseacrch = DB.getConnection();
            JasperPrint jasperprint = JasperFillManager.fillReport(jaspareport, params, connseacrch);
            JasperViewer.viewReport(jasperprint, false);
            //connseacrch.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        if (!Custo.getSelectedItem().equals("~Select~")) {
            String cst = Custo.getSelectedItem().toString().split(" ")[0];
            try {
                int i = 0;
                ResultSet rs = Database.DB.search("select* from invoice_aod where id_customer='" + cst + "' and state='3'");
                while (rs.next()) {
                    Vector v = new Vector();

                    v.add((++i + "."));
                    v.add(rs.getString("id_invoice"));
                    v.add(rs.getString("employee_salesrep"));
                    v.add(rs.getDouble("sub_total") + 0.00);
                    v.add(rs.getDouble("net_total") + 0.00);
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        getTotal();
    }

    public void getTotal() {
        Double sumb = 0.00;
        Double sump = 0.00;
        for (int i = 0; jTable1.getRowCount() > i; i++) {
            if (jTable1.getValueAt(i, 4) != null) {
                if(!jTable1.getValueAt(i, 4).toString().trim().equals("")){
                sumb = sumb + Double.parseDouble(jTable1.getValueAt(i, 4).toString());
                }
            }
             
            if (jTable1.getValueAt(i, 5) != null) {
                if(!jTable1.getValueAt(i, 5).toString().trim().equals("")){
                sump = sump + Double.parseDouble(jTable1.getValueAt(i, 5).toString());
            }
            }
            tbalance.setText(sumb + "");
            tpaid.setText(sump + "");
            nbalance.setText(sumb - sump + "");
            jTextField6.setText(sump + "");
        }

    }

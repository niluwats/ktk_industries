    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        if ((!cash.isSelected() || !chq.isSelected()) && Custo.getSelectedItem().toString().equals("~Select~")) {
            JOptionPane.showMessageDialog(this, "Please Check the Details again");
        } else {

            if (Double.parseDouble(tpaid.getText()) == 0.0 || jTable1.getRowCount() < 1) {
                JOptionPane.showMessageDialog(this, "Please Do a payment First");
            } else {
                try {
                    String Cusid=Custo.getSelectedItem().toString().split(" ")[0];
                    String selected="";
                    if(cash.isSelected()){
                        selected="Cash";
                            
                    }else{
                    selected="Cheque";
                    }
                   Database.DB.iud("insert into receipts values ('"+RecNo.getText()+"','"+Cusid+"','"+selected+"','"+lb_date.getText()+"','"+Double.parseDouble(tpaid.getText())+"','1')");
                   
                } catch (Exception e) {
                    e.printStackTrace();
                }
                DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
                
                try {
                    for (int i=0;i<dtm.getRowCount();i++){
                        if(dtm.getValueAt(i, 5)!=null){
                            
                        if(Integer.parseInt(dtm.getValueAt(i, 5).toString())!=0){
                          double remainder=Double.parseDouble(dtm.getValueAt(i, 4).toString())-Double.parseDouble(dtm.getValueAt(i, 5).toString());
                         Database.DB.iud("insert into receipts_has_aod (id_receipts,id_invoice) values ('"+RecNo.getText()+"','"+dtm.getValueAt(i, 1).toString()+"')");
                         String stat="3";
                        if (remainder==0.0){
                           Database.DB.iud("update invoice_aod set net_total='0.00',state='5' where id_invoice='"+dtm.getValueAt(i, 1).toString()+"'");
                        }else{
                        Database.DB.iud("update invoice_aod set net_total='"+remainder+"' where id_invoice='"+dtm.getValueAt(i, 1).toString()+"'");
                        }
                        }
                        }
                        
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
                new Model.Save().setVisible(true);
                dtm.setRowCount(0);
                ReportGen();
                jTextField6.setText("0.00");
                tbalance.setText("0.0");
                tpaid.setText("0.0");
                nbalance.setText("0.0");
                loadNo();
                    loadcust();
            }
        }
                 
    }//GEN-LAST:event_btn_SaveActionPerformed

